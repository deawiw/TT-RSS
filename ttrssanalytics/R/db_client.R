  if (length(missing_columns) > 0L) {
    fail(
      sprintf(
        "Р СңР ВөР РҶР С•Р В·Р СҳР С•Р В¶Р Р…Р С• Р С—Р С•Р Т‘Р С–Р С•РЎвҖҡР С•Р РҶР С‘РЎвҖҡРЎРҠ Р Т‘Р В°РЎвҖҡР В°РЎвҖһРЎРӮР ВөР в„–Р Сҳ Р С” Р В·Р В°Р С–РЎРӮРЎС“Р В·Р С”Р Вө Р РҶ PostgreSQL: Р Р…Р Вө РЎвҖҰР РҶР В°РЎвҖҡР В°Р ВөРЎвҖҡ Р С”Р С•Р В»Р С•Р Р…Р С•Р С” %s.",
        paste(missing_columns, collapse = ", ")
      )
    )
  }

  if (nrow(articles_df) == 0L) {
    return(data.frame(
      news_id = numeric(),
      source = character(),
      title = character(),
      content_text = character(),
      published_at = as.POSIXct(character(), tz = "UTC"),
      url = character(),
      topic_tags = character(),
      extracted_at = as.POSIXct(character(), tz = "UTC"),
